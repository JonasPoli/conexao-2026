    // News Background Animation Interaction (v1 dynamic behavior)
    (function() {
        const section = document.querySelector('section.bg-slate-200.relative.overflow-hidden');
        const bg = document.getElementById('news-bg-container');
        if (!section || !bg) return;
        section.addEventListener('mousemove', (e) => {
            const rect = section.getBoundingClientRect();
            const x = ((e.clientX - rect.left) / rect.width - 0.5) * 20;
            const y = ((e.clientY - rect.top) / rect.height - 0.5) * 20;
            bg.style.transform = `translate(${x}px, ${y}px)`;
        });
        section.addEventListener('mouseleave', () => { bg.style.transform = 'translate(0, 0)'; });
    })();
